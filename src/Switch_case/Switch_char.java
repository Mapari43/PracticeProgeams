package Switch_case;

public class Switch_char {

	public static void main(String[] args) {
		
		System.out.println("To check a perticular vowel in Alphabet's");
				
		char c ='o';
		
		switch (c)
		{
		case 'a':
			System.out.println("C "+"is a vowel");
			break;
			
		case 'e':
			System.out.println("C "+"is a vowel");
			break;
			
		case 'i':
			System.out.println("C "+"is a vowel");
			break;
			
		case 'o':
			System.out.println("C "+"is a vowel");
			break;
			
		case 'u':
			System.out.println("C "+"is a vowel");
			break;
			
			default:
				System.out.println("C "+"is not a vowel");	
		}
	}

}
