package Switch_case;

public class If_IfElse {

	public static void main(String[] args) {
		int money = 200;
		if(money>2000 && money<5000)
		{
			System.out.println("I will buy a mobile");
		}
		else if (money>10000 && money<3000)
		{
			System.out.println("I will buy a Laptop");
		}
		else
		{
			System.out.println("Insufficient money");
		}
	}

}
